void handle_error(const char *message) {

    // Print the error message to the debug console
    printf("ERROR: %s\n\r", message);

    while (1) {
        // Toggle an LED to indicate an error (assuming LED_PIN is configured)
        GPIOA->ODR ^= LED_PIN;  // Toggle LED
        for (volatile int i = 0; i < 1000000; i++);  // Delay loop
    }

}



/* Computed data buffer */
uint8_t computed_encryption[sizeof(expected_encrypted_text)];
uint8_t computed_text[sizeof(message)];

int main()
{

	cmox_cipher_retval_t  retval;
	size_t computed_size;

	/*Enable FPU*/
	fpu_enable();

	/*Initialize debug UART*/
	debug_uart_init();

	/*Initialize timebase*/
	timebase_init();

	/*Initialize LED*/
	led_init();

	/*Initialize Push button*/
	button_init();

	/*Initialize ADC*/
	pa1_adc_init();

	/*Start conversion*/
	start_conversion();

	/*Initialize cryptolib*/
	if(cmox_initialize(NULL) !=  CMOX_INIT_SUCCESS)
	{

     handle_error("ERROR: Init failed\r\n");


	}


	cmox_rsa_construct(&rsa_ctx, CMOX_RSA_MATH_FUNCS, CMOX_MODEXP_PUBLIC, working_buffer, sizeof(working_buffer));

    /* Fill in RSA key structure using the public key representation */
	retval = cmox_rsa_setKey(&rsa_key,                                      /* RSA key structure to fill */
	                           modulus, sizeof(modulus),                      /* public key modulus */
							   public_exponent, sizeof(public_exponent));   /* public key exponent */

	/* Verify API returned value */
	   if (retval != CMOX_RSA_SUCCESS)
	   {
			  handle_error("RSA key set failed!");

	   }


	   /* Compute directly the encrypted message passing all the needed parameters */
	   retval = cmox_rsa_pkcs1v22_encrypt(&rsa_ctx,                                 /* RSA context */
	                                      &rsa_key,                                 /* RSA key to use */
	                                      message, sizeof(message),                 /* Message to encrypt */
	                                      CMOX_RSA_PKCS1V22_HASH_SHA1,              /* Hash method to use */
	                                      seed, sizeof(seed),                       /* Random seed */
	                                      NULL, 0,                                  /* No label */
	                                      computed_encryption, &computed_size);  /* Data buffer to receive encrypted text */



	   /* Verify API returned value */
	   if (retval != CMOX_RSA_SUCCESS)
	   {
			 handle_error("RSA encryption failed!");

	   }


	   /* Verify generated data are the expected ones */
	   if (memcmp(computed_encryption, expected_encrypted_text, computed_size) != 0)
	   {
			 handle_error("RSA encryption content mismatch!");

	   }


	   /* Cleanup context */
	   cmox_rsa_cleanup(&rsa_ctx);
	while(1)
	{

	}
}

